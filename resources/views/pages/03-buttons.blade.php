<h1>Using buttons</h1>
<h3 class="mt-4">The default button</h3>
<p>When using buttons on most common pages - the main style to used is outlined below. Unless you have a good reason this is the button to use.</p> 
<p> How it looks:</p>
<blockquote>
    
    <button class="btn btn-primary">Standard Button</button>

</blockquote>

<pre>
	
	&lt;button class=&quot;btn btn-primary&quot;&gt;Standard Button&lt;/button&gt;

</pre>

<h3 class="mt-4">The inverse button</h3>
<p>Sometimes we need a button that takes less attention or that is used on a alternative background.</p> 
<p>How it looks:</p>
<blockquote>
    
    <button class="btn btn-secondary">Secondary Button</button>

</blockquote>

<pre>
	
	&lt;button class=&quot;btn btn-secondary&quot;&gt;Secondary Button&lt;/button&gt;

</pre>