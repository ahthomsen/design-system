<h1>Using icons</h1>
<h3>Introduction</h3>
<p>Icons play a big role in our world. Why this must be done properly.</p>
<h3>General icon guidelines to follow:</h3>
<ul class="list list-unordered">
  <li class="list-item">Only use line icons - with the same line width</li>
  <li class="list-item">Only use icons within the main color scheme</li>
  <li class="list-item">Keep icons simple - do not include more advanced icons unless part of a larger illustration</li>
  <li class="list-item">Only "complete" line icons</li>
  <li class="list-item">Ensure high contrast when using backgrounds</li>
</ul>
